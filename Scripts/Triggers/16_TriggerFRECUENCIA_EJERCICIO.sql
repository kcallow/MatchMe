CREATE OR REPLACE TRIGGER beforeInsert_frecuencia_ejercicio
       BEFORE INSERT
       ON GE.FRECUENCIA_EJERCICIO FOR EACH ROW
BEGIN
  :NEW.USUARIO_CREACION:= USER;
  :NEW.FEC_CREACION:= SYSDATE;
END;
CREATE OR REPLACE TRIGGER beforeUpdate_frecuencia_ejercicio
       BEFORE UPDATE
       ON GE.FRECUENCIA_EJERCICIO FOR EACH ROW
BEGIN
  :NEW.USUARIO_ULTIMA_MOD:= USER;
  :NEW.FEC_ULTIMA_MOD:= SYSDATE;
END;
